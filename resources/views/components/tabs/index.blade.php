{{--
==============================================================
 WP Components: Tabs
==============================================================

Универсальные табы для переключения контента.
Построены на Alpine.js (встроенный x-data, не требуют script.js).

--------------------------------------------------------------
 1. ДОЧЕРНИЕ КОМПОНЕНТЫ
--------------------------------------------------------------
 <x-tabs>           : Главный контейнер (хранит состояние).
 <x-tabs.list>      : Контейнер для кнопок.
 <x-tabs.trigger>   : Кнопка переключения (обязателен пропс value).
 <x-tabs.content>   : Блок контента (обязателен пропс value).

--------------------------------------------------------------
 2. ПРИМЕР ИСПОЛЬЗОВАНИЯ
--------------------------------------------------------------

 <x-tabs default="settings">
     <x-tabs.list>
         <x-tabs.trigger value="settings">Настройки</x-tabs.trigger>
         <x-tabs.trigger value="billing">Оплата</x-tabs.trigger>
     </x-tabs.list>

     <x-tabs.content value="settings">
         Здесь настройки профиля...
     </x-tabs.content>
     
     <x-tabs.content value="billing">
         Здесь история оплат...
     </x-tabs.content>
 </x-tabs>

--------------------------------------------------------------
 3. ПРИМЕЧАНИЯ
--------------------------------------------------------------
 - Если не передать пропс default в <x-tabs>, автоматически 
   откроется первая вкладка.
 - Табы адаптивны: на мобильных кнопки перенесутся на новую 
   строку без ломания верстки.
 - Добавлена микро-анимация появления контента (x-transition).
==============================================================
--}}

@props([
    'default' => null,
    'class'   => null,
])

<div 
    x-data="{ 
        activeTab: '{{ $default }}' 
    }" 
    class="{{ cn($class) }}"
>
    {{ $slot }}
</div>