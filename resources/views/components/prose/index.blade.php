{{--
==============================================================
 WP Components: Prose
==============================================================

Компонент для стилизации "сырого" HTML-контента (статьи блога,
описания из WYSIWYG-редакторов). 

Заменяет плагин tailwindcss/typography, применяя чистый CSS
ко всем типографическим тегам (h1-h4, p, ul, blockquote, code).

--------------------------------------------------------------
 1. ПРОПСЫ
--------------------------------------------------------------
 as    (string) : Тег обертки. По умолчанию: 'article'.
 size  (string) : Размер шрифта ('sm', 'base', 'lg'). 
                  По умолчанию: 'base'.
 class (string) : Доп. классы.

--------------------------------------------------------------
 2. ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ
--------------------------------------------------------------

 // 1. Вывод статьи блога (увеличенный шрифт для читабельности)
 <x-prose size="lg">
     @php(the_content())
 </x-prose>

 // 2. Вывод текста из произвольного поля (ACF)
 <x-prose>
     @php(get_field('about_text'))
 </x-prose>

 // 3. Снять ограничение максимальной ширины (max-w-none)
 <x-prose class="max-w-none">
     @php(the_content())
 </x-prose>
==============================================================
--}}
@props([
    'as' => 'article',
    'size' => 'base',
    'class' => null,
])

@php
    $sizes = [
        'sm' => 'prose-sm',
        'base' => 'prose-base',
        'lg' => 'prose-lg',
    ];
    $sizeClass = $sizes[$size] ?? $sizes['base'];
@endphp

<{{ $as }} class="{{ cn('wp-prose', $sizeClass, $class) }}">
    {{ $slot }}
</{{ $as }}>