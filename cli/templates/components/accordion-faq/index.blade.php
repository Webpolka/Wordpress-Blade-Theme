{{--
  ============================================================
  Компонент: Accordion Faq
  Описание: Аккордеон для блока "Часто задаваемые вопросы (FAQ)".
             Построен на Alpine.js + плагине @alpinejs/collapse.
  ============================================================

  ------------------------------------------------------------
  ОСОБЕННОСТИ
  ------------------------------------------------------------
    • По умолчанию каждый вопрос — это отдельная карточка (плашечка)
      с тенями и скруглениями.
    • Иконка-плюс поворачивается на 45° (превращается в крестик)
      при открытии.
    • Плавная анимация высоты без скачков верстки.
    • Полная поддержка доступности (a11y): aria-expanded, aria-controls.

  ------------------------------------------------------------
  ПРОПСЫ (Параметры родителя)
  ------------------------------------------------------------
    - multiple  : bool   – Разрешить открывать несколько вопросов 
                           одновременно. По умолчанию: false.
    - firstOpen : bool   – Автоматически открыть первый вопрос при 
                           загрузке страницы. По умолчанию: false.
    - flush     : bool   – Плоский дизайн (убрать карточки, сделать 
                           сплошной список с разделителями). 
                           По умолчанию: false.
    - class     : string – Дополнительные CSS-классы для обертки.

  ------------------------------------------------------------
  ДОЧЕРНИЕ КОМПОНЕНТЫ
  ------------------------------------------------------------
    <x-accordion-faq.item> : Пара "Вопрос - Ответ"
      Пропсы:
        - question (string) : Текст вопроса.
        - class    (string) : Доп. классы для карточки.
      Слоты:
        - default  : Текст ответа.
        - question : Кастомный HTML для вопроса (переопределяет пропс).

  ============================================================
  ПРИМЕРЫ ИСПОЛЬЗОВАНИЯ
  ============================================================

  1. СТАНДАРТНЫЙ (Отдельные плашечки, открыт один):
    <x-accordion-faq>
        <x-accordion-faq.item question="Сколько стоит доставка?">
            Доставка бесплатна при заказе от 5000 руб.
        </x-accordion-faq.item>
        <x-accordion-faq.item question="Как вернуть товар?">
            Вернуть товар можно в течение 14 дней.
        </x-accordion-faq.item>
    </x-accordion-faq>

  2. С АВТООТКРЫТИЕМ ПЕРВОГО ВОПРОСА:
    <x-accordion-faq :first-open="true">
        ...
    </x-accordion-faq>

  3. С ВОЗМОЖНОСТЬЮ ОТКРЫТЬ ВСЕ СРАЗУ (Multiple mode):
    <x-accordion-faq :multiple="true">
        ...
    </x-accordion-faq>

  4. ПЛОСКИЙ ДИЗАЙН (Список без карточек):
    <x-accordion-faq :flush="true">
        ...
    </x-accordion-faq>

  5. КАСТОМНЫЙ HTML В ВОПРОСЕ (Через слот):
    <x-accordion-faq>
        <x-accordion-faq.item>
            <x-slot:question>
                <span class="text-blue-600">Важно:</span> Как вернуть товар?
            </x-slot:question>
            Ответ здесь...
        </x-accordion-faq.item>
    </x-accordion-faq>
--}}



@props([
    'multiple'  => false,
    'firstOpen' => false,
    'flush'     => false,
    'class'     => null,
])

@php
    $wrapperClasses = cn(
        $flush ? 'divide-y divide-gray-200 dark:divide-gray-700' : 'space-y-4',
        $class
    );
@endphp

<div
    x-data="accordionFaq({
        multiple: {{ $multiple ? 'true' : 'false' }},
        firstOpen: {{ $firstOpen ? 'true' : 'false' }}
    })"
    class="{{ $wrapperClasses }}"
>
    {{ $slot }}
</div>